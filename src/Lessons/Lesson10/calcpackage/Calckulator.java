package Lessons.Lesson10.calcpackage;

public class Calckulator {


    public int gumarum(int a, int b) {
        return a + b;
    }

    public int hanum(int a, int b) {
        return a - b;
    }

    public double bajanum(double a, double b) {
        return a / b;
    }

    public long bazmapatkum(int a, int b) {
        return a * b;
    }

}
